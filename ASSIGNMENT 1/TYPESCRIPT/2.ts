//Write a program to declare two numbers and display: Addition, Subtraction, Multiplication, Division


let a:number = 5;
let b:number = 10;

let add:number = a + b;
let sub:number = a - b;
let mult:number = a * b;
let div:number = a / b;

console.log(`Addition of ${a} + ${b} = ${add}`);
console.log(`Subtraction of ${a} - ${b} = ${sub}`);
console.log(`Multiplication of ${a} * ${b} = ${mult}`);
console.log(`Division of ${a} / ${b} = ${div}`);

/* Addition of 5 + 10 = 15
Subtraction of 5 - 10 = -5
Multiplication of 5 * 10 = 50
Division of 5 / 10 = 0.5 */